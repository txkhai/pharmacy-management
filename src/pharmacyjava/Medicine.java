    private void MedTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MedTableMouseClicked
        DefaultTableModel model = (DefaultTableModel)MedTable.getModel();
        int MyIndex = MedTable.getSelectedRow();
        MedID.setText(model.getValueAt(MyIndex, 0).toString());
        MedName.setText(model.getValueAt(MyIndex, 1).toString());
        MedPrice1.setText(model.getValueAt(MyIndex, 2).toString());
        MedPrice2.setText(model.getValueAt(MyIndex, 3).toString());        
        MedQty.setText(model.getValueAt(MyIndex, 4).toString());
        MedNSX.setDate((Date) model.getValueAt(MyIndex, 5));
        MedHSD.setDate((Date) model.getValueAt(MyIndex, 6));
        MedComp.setSelectedItem(model.getValueAt(MyIndex, 7));
        if(model.getValueAt(MyIndex, 8) == null)
            MedNote.setText("");
        else
            MedNote.setText(model.getValueAt(MyIndex, 8).toString());
    }//GEN-LAST:event_MedTableMouseClicked

