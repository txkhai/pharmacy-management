    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        if(MedID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nhập ID thuốc cần xóa");
        }else{
            try {
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Pharmadb", "User1", "IoTET103");
                String Id = MedID.getText();
                String Query = "DELETE FROM User1.MEDICINETBL WHERE ID = " + Id;
                Statement Del = Con.createStatement();
                Del.executeUpdate(Query);
                SelectMed();
                JOptionPane.showMessageDialog(this, "Đã xóa khỏi danh sách");
            } catch (SQLException ex) {
                Logger.getLogger(Medicine.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

